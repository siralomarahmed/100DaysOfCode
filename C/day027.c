#include <stdio.h>

// Function Call Statement
int main()
{
  // Variable deceleration statements
  int x;
  int box;

  // Variable initiation statement
  box = 25;

  // Condition statement with a logical operator
  x = box >= 13 && box <= 19;

  // Print statement
  printf("x = %d \n", x);

  // return statement
  return 0;
}
