# Extract a character from string.

story = "Once upon a time in a land far, far away"

# To find the length
p story.length

# Example of calling an index number in range of the string length.
p story[2]

# Example to call an index less than zero.
p story[-1]

# Example of calling an index that is more than the length of the string
p story[100]

# Example of using the .slice method
p story.slice(5)
