# Intermediate Variable - Parallel Variable

a = 10
b = 20
c = 30

# Instead of writing everything on a separate line like this example here:
p a
p b
p c

puts

# You can write all the variables in one line, comma seperated.
p a, b, c

puts

# You can use print so they are all in the same line when you output them
print a, b, c
