int main()
{
  // initilize an array with a value
  char first_name[] = {"John"};

  // Reassigning the values of the array
  first_name[0] = 'M';
  first_name[1] = 'i';
  first_name[2] = 'k';
  first_name[3] = 'e';
  
  return 0;
}
