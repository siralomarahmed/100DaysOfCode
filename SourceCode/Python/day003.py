# Boolean
True
False

print(True)
print(type(True))
print(False)
print(type(False))

# Integer Number
2

print(2)
print(type(2))

# Float Number
3.14

print(3.14)
print(type(3.14))

# Character
'H'

print('H')
print(type('H'))

# String
"Computer Science"

print("Computer Science")
print(type("Computer Science"))
