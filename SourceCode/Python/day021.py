# Comparison Operators:
## Equal
print(2 == 2)
print(2 == 1)

## Not Equal
print(2 != 2)
print(2 != 1)

## Greater than
print(3 > 2)
print(3 > 4)
print(3 > 3)

## Greater than or equal
print(3 >= 2)
print(3 >= 4)
print(3 >= 3)

## Less than
print(3 < 2)
print(3 < 4)
print(3 < 3)

## Less than or equal
print(3 <= 2)
print(3 <= 4)
print(3 <= 3)


# Logical operators:
## AND
print(True and True)
print(True and False)
print(False and False)

## OR
print(True or True)
print(True or False)
print(False or False)

## NOT
print(not True)
print(not False)
