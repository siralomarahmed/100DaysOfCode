# Some variables
first_name = "Ahmed"
last_name = "Alomar"

# String concatenation using the plus operator
print("Hello there, " + first_name + ", your last name is: " + last_name + ".")

# String concatenation using the pre-formatted string techneque
print(f"Hello there, {first_name}, Your last name is: {last_name}.")
