# Assign
rank = 12
print(rank)

# Add and Assign
rank += 1
print(rank)

# Subtract and Assign
rank -= 1
print(rank)

# Multiply and Assign
rank *= 2
print(rank)

# Exponent and Assign
rank **= 2
print(rank)

# Divide and Assign
rank /= 2
print(rank)

# Floor Division and assign
rank //= 2
print(rank)

# Modulus and Assign
rank %= 7
print(rank)
